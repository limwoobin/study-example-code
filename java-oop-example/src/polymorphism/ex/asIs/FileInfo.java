package polymorphism.ex.asIs;

public class FileInfo {
    private CloudId cloudId;
    private String fileId;
    private String name;
    private long length;

    public CloudId getCloudId() {
        return cloudId;
    }

    public String getFileId() {
        return fileId;
    }

    public String getName() {
        return name;
    }

    public long getLength() {
        return length;
    }
}
